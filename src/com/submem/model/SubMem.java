package com.submem.model;

import java.io.Serializable;

public class SubMem implements Serializable{
	
	private Integer actSubMemNo;
	private Integer beSubMemNo;
	private Integer subState;
	
	
	public Integer getActSubMemNo() {
		return actSubMemNo;
	}
	public void setActSubMemNo(Integer actSubMemNo) {
		this.actSubMemNo = actSubMemNo;
	}
	public Integer getBeSubMemNo() {
		return beSubMemNo;
	}
	public void setBeSubMemNo(Integer beSubMemNo) {
		this.beSubMemNo = beSubMemNo;
	}
	public Integer getSubState() {
		return subState;
	}
	public void setSubState(Integer subState) {
		this.subState = subState;
	}
	
	
	
	

}
